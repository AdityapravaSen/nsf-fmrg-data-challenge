"""12_geometry_descriptor_implementation.py

Experiment 12: Descriptor Implementation.

Implements the frozen geometry descriptor from Experiments 10--11 on the
thermal-anchor grid in processed_data/phase1_unified_master.csv.

Descriptor components:
- normalized cross-sectional shape represented by PCA scores
- local amplitude / relief
- signed elevation
- validity / finite-support metadata
- regime metadata

Track 21 remains sealed. This script only processes Tracks 8, 10, and 14.
"""

